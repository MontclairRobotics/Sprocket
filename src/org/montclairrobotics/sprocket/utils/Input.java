package org.montclairrobotics.sprocket.utils;

public class Input {

    private double value;

    public Input(double value) {
        this.value = value;
    }

    public Input() {
        this.value = 0.0;
    }


    public double get() {
        return get();
    }

    public void set(double value) {
        this.value = value;
    }
}
